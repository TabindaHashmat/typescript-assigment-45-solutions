function cityCountry(city, country) {
    return "".concat(city, ", ").concat(country);
}
// Call the function with at least three city-country pairs
var location1 = cityCountry('Lahore', 'Pakistan');
var location2 = cityCountry('Tokyo', 'Japan');
var location3 = cityCountry('Sydney', 'Australia');
// Print the values returned by the function
console.log(location1);
console.log(location2);
console.log(location3);
